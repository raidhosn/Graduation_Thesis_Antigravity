    <p>In the industrial era, profane time was standardized and fragmented. The clock was replacing the natural rhythm of pre-modern cultures, the industrial way of life made humanity's relationship with the seasonal cycle less urgent, thus weakening the orientation of sacred time. In a critique of Marxism, MAFFESOLI (1995, p. 31) proposes to think contemporary culture, observing the emergence of new post-industrial values. With post-industrialism, such values emerge, such as: the connection between globalism and tribalism, small groups or "tribes," archaism and the technological, communication, shared emotion, hedonism, mysticism, among other categories. For electronic relationships transcend uniform measures of time, thus freeing individuals from obedience to the standardized and fragmented clock and showing openness to the new rhythms and new cycles implied by Cybertime, which, after all, is the sacred time of the post-industrial era.</p>

