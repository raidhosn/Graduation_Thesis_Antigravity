    <p>In archaic societies, time is ahistorical, non-linear, governed by mystical experience and not by the succession of events. Primitive man has a connection with the cosmos, with abstract time. Primitives lived in a "continuous present." Time is natural, marked by fire, water, and earth. Measured by cycles: seasonal, solar, menstrual, etc. The cyclical character, in which time begins again, passes through the regeneration and updating of phenomena proper to the people and situations of these communities.</p>

